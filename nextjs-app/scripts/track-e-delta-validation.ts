/**
 * Wave 1 Track A — v2.1 methodology re-validation against the 64 rescraped utilities.
 *
 * Compares Calculator outputs (bill impact, revenue adequacy ratio, demand-charge
 * flow-through %) using:
 *   - Baseline tariff data: `lib/generatedTariffData.baseline-jan2026.ts`
 *     (Jan 2026 snapshot extracted from SHA `ab74e83`, pre-Hermes-rescrape;
 *     this is what Gemini originally reviewed for the v2.1 methodology bless)
 *   - Refreshed tariff data: `lib/generatedTariffData.ts` (current — 64 rescraped
 *     via Hermes 2026-05-14 / 2026-05-17, 24 still on Jan 2026 baseline)
 *
 * Utilities included: every entry in the current generatedTariffData.ts whose
 * `last_verified ∈ { '2026-05-14', '2026-05-17' }` — the rescraped 64.
 *
 * Scenarios per utility (5):
 *   1. 1 GW firm 24/7 baseload (LF 0.95, peak coincidence 1.0)
 *   2. 1 GW flexible (LF 0.95, peak coincidence 0.75)
 *   3. DC penetration at 5% of systemPeakMW (firm baseload)
 *   4. DC penetration at 10% of systemPeakMW (firm baseload)
 *   5. DC penetration at 20% of systemPeakMW (firm baseload)
 *
 * Outputs:
 *   - `docs/track-e-deltas-2026-05-27.json` — full delta matrix (raw)
 *   - stdout — concise summary; `docs/GEMINI_REFRESH_DELTA_2026-05-27.md` is
 *     drafted by hand from the JSON, not auto-generated by this script
 *
 * Usage: `npx tsx scripts/track-e-delta-validation.ts`
 */

