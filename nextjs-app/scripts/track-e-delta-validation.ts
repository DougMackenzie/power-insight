type EnrichedLike = (typeof GENERATED_TARIFFS)[number];

function buildProfileFromPool(
    utilityId: string,
    tariffPool: EnrichedLike[],
): UtilityProfile | undefined {
    const manual = UTILITY_PROFILES.find((u) => u.id === utilityId);
    const tariff = tariffPool.find((t) => t.id === utilityId);

    if (manual && tariff) {
        const peakDemandCharge = (tariff.peak_demand_charge || 5) * 1000;
        const maxDemandCharge =
            (tariff.off_peak_demand_charge || tariff.peak_demand_charge * 0.4) * 1000;
        const energyCharge = tariff.blendedRatePerKWh * 1000;
        const ratchetPercent = tariff.protections.ratchet_pct
            ? tariff.protections.ratchet_pct / 100
            : manual.tariff.ratchetPercent;

        return {
            ...manual,
            state: tariff.state,
            tariff: {
                ...manual.tariff,
                peakDemandCharge,
                maxDemandCharge,
                energyCharge,
                ratchetPercent,
                ratchetMonths: ratchetPercent ? 12 : manual.tariff.ratchetMonths,
                tariffSource: `${tariff.tariff_name} (${tariff.rate_schedule}); ${manual.tariff.tariffSource}`,
            },
        };
    }
    if (manual) return manual;
    if (tariff) return enrichedTariffToUtilityProfile(tariff);
    return undefined;
}

function buildUtilityFromProfile(profile: UtilityProfile): Utility {
    return {
        ...DEFAULT_UTILITY,
        name: profile.name,
        state: profile.state,
        residentialCustomers: profile.residentialCustomers,
        averageMonthlyBill: profile.averageMonthlyBill,
        averageMonthlyUsage: profile.averageMonthlyUsageKWh,
        systemPeakMW: profile.systemPeakMW,
        baseResidentialAllocation: profile.market.baseResidentialAllocation,
        marketType: profile.market.type,
        hasCapacityMarket: profile.market.hasCapacityMarket,
        capacityCostPassThrough: profile.market.capacityCostPassThrough,
        capacityPrice2024: profile.market.capacityPrice2024,
        totalGenerationCapacityMW: profile.totalGenerationCapacityMW,
        currentReserveMargin: profile.currentReserveMargin,
        interconnection: profile.interconnection,
        marginalEnergyCost: profile.market.marginalEnergyCost,
    };
}

function buildDataCenter(scenario: Scenario, capacityMW: number): DataCenter {
    if (scenario.series === 'flexible') {
        return {
            ...DEFAULT_DATA_CENTER,
            capacityMW,
            firmLoadFactor: 0.80,
            firmPeakCoincidence: 1.0,
            flexLoadFactor: scenario.loadFactor,
            flexPeakCoincidence: scenario.peakCoincidence,
            onsiteGenerationMW: 0,
        };
    }
    return {
        ...DEFAULT_DATA_CENTER,
        capacityMW,
        firmLoadFactor: scenario.loadFactor,
        firmPeakCoincidence: scenario.peakCoincidence,
        flexLoadFactor: scenario.loadFactor,
        flexPeakCoincidence: scenario.peakCoincidence,
        onsiteGenerationMW: 0,
    };
}

// ============================================================
// RUN ONE UTILITY × SCENARIO × DATASET
// ============================================================
interface RunResult {
    capacityMW: number;
    monthlyBillFinal: number;
    baselineFinal: number;
    dcImpactMonthly: number;
    revAdequacyRatio: number;
    surplusOrDeficitPerMW: number;
    totalRevenue: number;
    totalCost: number;
    demandRevenue: number;
    energyRevenue: number;
    capacityCost: number;
    demandChargeFlowThroughPct: number; // What share of demand revenue flows through to ratepayers
}

function computeDemandFlowThroughPct(utility: Utility): number {
    // Mirrors calculations.ts:1307–1309 logic — kept inline so we can report
    // the flow-through % alongside scenario results without re-running the
    // private bill-impact path.
    const isRegulatedMarket = !utility.hasCapacityMarket && utility.marketType !== 'ercot';
    const base = isRegulatedMarket ? 0.90 : utility.marketType === 'ercot' ? 0.70 : 0.60;
    const HIGH_NBC_STATES = ['CA', 'NY', 'CT', 'MA', 'RI', 'NH'];
    const passthrough = HIGH_NBC_STATES.includes(utility.state) ? 0.60 : 1.0;
    return base * passthrough;
}

function runOne(
    profile: UtilityProfile,
    scenario: Scenario,
): RunResult | { error: string } {
    try {
        const utility = buildUtilityFromProfile(profile);
        const capacityMW = scenario.capacityResolver(profile);
        const dc = buildDataCenter(scenario, capacityMW);
        const tariff = profile.tariff;

        const trajectories = generateAllTrajectories(
            utility,
            dc,
            PROJECTION_YEARS,
            tariff,
            ESCALATION,
        );

        const target =
            scenario.series === 'flexible' ? trajectories.flexible : trajectories.unoptimized;
        const baseline = trajectories.baseline;
        const finalIdx = target.length - 1;
        const monthlyBillFinal = target[finalIdx].monthlyBill;
        const baselineFinal = baseline[finalIdx].monthlyBill;

        const ra: RevenueAdequacyResult = calculateRevenueAdequacy(
            capacityMW,
            scenario.loadFactor,
            scenario.peakCoincidence,
            tariff,
            utility,
            0,
        );

        const flowThrough = computeDemandFlowThroughPct(utility);

        return {
            capacityMW,
            monthlyBillFinal,
            baselineFinal,
            dcImpactMonthly: monthlyBillFinal - baselineFinal,
            revAdequacyRatio: ra.revenueAdequacyRatio,
            surplusOrDeficitPerMW: ra.surplusOrDeficitPerMW,
            totalRevenue: ra.totalRevenue,
            totalCost: ra.totalMarginalCost,
            demandRevenue: ra.demandChargeRevenue,
            energyRevenue: ra.energyChargeRevenue,
            capacityCost: ra.marginalCapacityCost,
            demandChargeFlowThroughPct: flowThrough,
        };
    } catch (err: unknown) {
        const msg = err instanceof Error ? err.message : String(err);
        return { error: msg };
    }
}

