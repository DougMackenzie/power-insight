interface ScenarioDelta {
    scenario: string;
    kind: Scenario['kind'];
    capacity_mw: number | null;
    baseline: RunResult | { error: string };
    refreshed: RunResult | { error: string };
    delta: {
        bill_impact_abs: number | null;
        bill_impact_pct: number | null;
        rev_adequacy_ratio_abs: number | null;
        flow_through_pct_abs: number | null;
    };
    bucket: '<5%' | '5-10%' | '10-20%' | '>20%' | 'incalculable';
}

interface UtilityDelta {
    id: string;
    utility: string;
    state: string;
    iso_rto: string;
    last_verified_baseline: string;
    last_verified_refreshed: string;
    tariff_diff: Record<string, { baseline: unknown; refreshed: unknown }>;
    scenarios: ScenarioDelta[];
    max_abs_pct: number; // for sort
}

function bucketize(pct: number | null): ScenarioDelta['bucket'] {
    if (pct === null || !Number.isFinite(pct)) return 'incalculable';
    const a = Math.abs(pct);
    if (a < 0.05) return '<5%';
    if (a < 0.10) return '5-10%';
    if (a < 0.20) return '10-20%';
    return '>20%';
}

function pctDelta(baseline: number, refreshed: number): number | null {
    if (baseline === 0) return refreshed === 0 ? 0 : null;
    return (refreshed - baseline) / Math.abs(baseline);
}

function diffTariffFields(
    baseline: EnrichedLike,
    refreshed: EnrichedLike,
): UtilityDelta['tariff_diff'] {
    const fields: (keyof EnrichedLike)[] = [
        'peak_demand_charge',
        'off_peak_demand_charge',
        'energy_rate_peak',
        'energy_rate_off_peak',
        'blendedRatePerKWh',
        'min_load_mw',
        'data_center_specific',
    ];
    const out: UtilityDelta['tariff_diff'] = {};
    for (const f of fields) {
        const b = baseline[f];
        const r = refreshed[f];
        if (JSON.stringify(b) !== JSON.stringify(r)) {
            out[f as string] = { baseline: b, refreshed: r };
        }
    }
    // Also surface ratchet + take-or-pay (nested under protections)
    if (baseline.protections.ratchet_pct !== refreshed.protections.ratchet_pct) {
        out['protections.ratchet_pct'] = {
            baseline: baseline.protections.ratchet_pct,
            refreshed: refreshed.protections.ratchet_pct,
        };
    }
    if (baseline.protections.take_or_pay !== refreshed.protections.take_or_pay) {
        out['protections.take_or_pay'] = {
            baseline: baseline.protections.take_or_pay,
            refreshed: refreshed.protections.take_or_pay,
        };
    }
    if (baseline.protections.ciac_required !== refreshed.protections.ciac_required) {
        out['protections.ciac_required'] = {
            baseline: baseline.protections.ciac_required,
            refreshed: refreshed.protections.ciac_required,
        };
    }
    return out;
}

function main(): void {
    // Identify the 64 rescraped utilities
    const rescraped = GENERATED_TARIFFS.filter(
        (t) => t.last_verified && RESCRAPED_VERIFIED_DATES.has(t.last_verified),
    );
    console.error(`Detected ${rescraped.length} rescraped utilities`);

    const results: UtilityDelta[] = [];
    const utilityErrors: string[] = [];

    for (const refreshedTariff of rescraped) {
        const id = refreshedTariff.id;
        const baselineTariff = BASELINE_TARIFF_DATA.find((t) => t.id === id);

        if (!baselineTariff) {
            utilityErrors.push(
                `${id}: not present in Jan 2026 baseline (may be a post-Jan add) — skip`,
            );
            continue;
        }

        const refreshedProfile = buildProfileFromPool(id, GENERATED_TARIFFS as EnrichedLike[]);
        const baselineProfile = buildProfileFromPool(id, BASELINE_TARIFF_DATA as EnrichedLike[]);
        if (!refreshedProfile || !baselineProfile) {
            utilityErrors.push(
                `${id}: profile build failed (baseline=${!!baselineProfile}, refreshed=${!!refreshedProfile}) — skip`,
            );
            continue;
        }

        const tariff_diff = diffTariffFields(
            baselineTariff as EnrichedLike,
            refreshedTariff as EnrichedLike,
        );

        const scenarios: ScenarioDelta[] = [];
        let maxAbsPct = 0;

        for (const scenario of SCENARIOS) {
            const baselineRun = runOne(baselineProfile, scenario);
            const refreshedRun = runOne(refreshedProfile, scenario);

            let billDeltaAbs: number | null = null;
            let billDeltaPct: number | null = null;
            let raDeltaAbs: number | null = null;
            let ftDeltaAbs: number | null = null;
            let capacityMW: number | null = null;

            if ('error' in baselineRun || 'error' in refreshedRun) {
                // leave nulls
            } else {
                billDeltaAbs = refreshedRun.dcImpactMonthly - baselineRun.dcImpactMonthly;
                billDeltaPct = pctDelta(baselineRun.dcImpactMonthly, refreshedRun.dcImpactMonthly);
                raDeltaAbs = refreshedRun.revAdequacyRatio - baselineRun.revAdequacyRatio;
                ftDeltaAbs =
                    refreshedRun.demandChargeFlowThroughPct -
                    baselineRun.demandChargeFlowThroughPct;
                capacityMW = refreshedRun.capacityMW;
                if (billDeltaPct !== null && Math.abs(billDeltaPct) > maxAbsPct) {
                    maxAbsPct = Math.abs(billDeltaPct);
                }
            }

            scenarios.push({
                scenario: scenario.label,
                kind: scenario.kind,
                capacity_mw: capacityMW,
                baseline: baselineRun,
                refreshed: refreshedRun,
                delta: {
                    bill_impact_abs: billDeltaAbs,
                    bill_impact_pct: billDeltaPct,
                    rev_adequacy_ratio_abs: raDeltaAbs,
                    flow_through_pct_abs: ftDeltaAbs,
                },
                bucket: bucketize(billDeltaPct),
            });
        }

        results.push({
            id,
            utility: refreshedTariff.utility,
            state: refreshedTariff.state,
            iso_rto: refreshedTariff.iso_rto,
            last_verified_baseline: baselineTariff.last_verified ?? 'unknown',
            last_verified_refreshed: refreshedTariff.last_verified ?? 'unknown',
            tariff_diff,
            scenarios,
            max_abs_pct: maxAbsPct,
        });
    }

    // Aggregate bucket distribution
    const bucketCounts: Record<ScenarioDelta['bucket'], number> = {
        '<5%': 0,
        '5-10%': 0,
        '10-20%': 0,
        '>20%': 0,
        incalculable: 0,
    };
    let totalScenarios = 0;
    for (const u of results) {
        for (const s of u.scenarios) {
            bucketCounts[s.bucket]++;
            totalScenarios++;
        }
    }

    // Per-utility bucket: pick the worst scenario bucket for that utility (incalculable counts separately)
    const utilityBuckets: Record<string, number> = {
        '<5%': 0,
        '5-10%': 0,
        '10-20%': 0,
        '>20%': 0,
        incalculable: 0,
    };
    for (const u of results) {
        if (u.max_abs_pct === 0 && u.scenarios.every((s) => s.bucket === 'incalculable')) {
            utilityBuckets['incalculable']++;
            continue;
        }
        utilityBuckets[bucketize(u.max_abs_pct === 0 ? 0 : u.max_abs_pct > 0 ? u.max_abs_pct : 0)]++;
    }

    // Sort utilities by max delta pct desc (worst first)
    results.sort((a, b) => b.max_abs_pct - a.max_abs_pct);

    const summary = {
        generated: new Date().toISOString(),
        baseline_sha: 'ab74e83',
        baseline_description: 'Pre-Hermes-rescrape Jan 2026 (Records: 89, all last_verified 2026-01-31 or 2026-02-01)',
        refreshed_description: `Current main (Records: ${GENERATED_TARIFFS.length}, rescraped: ${rescraped.length})`,
        methodology_version: 'v2.1 (Gemini-blessed 2026-05-14)',
        rescraped_count_detected: rescraped.length,
        utility_results_count: results.length,
        scenarios_per_utility: SCENARIOS.length,
        total_scenarios_run: totalScenarios,
        scenario_bucket_counts: bucketCounts,
        utility_bucket_counts_by_worst_scenario: utilityBuckets,
        utility_errors: utilityErrors,
    };

    // Write JSON output
    const docsPath = join(__dirname, '..', '..', 'docs', 'track-e-deltas-2026-05-27.json');
    writeFileSync(
        docsPath,
        JSON.stringify({ summary, utilities: results }, null, 2),
        'utf8',
    );

    // Stdout summary
    console.log('# Wave 1 Track A — Delta Validation Summary');
    console.log('');
    console.log(`Generated: ${summary.generated}`);
    console.log(`Baseline SHA: ${summary.baseline_sha}`);
    console.log(`Rescraped utilities detected: ${summary.rescraped_count_detected}`);
    console.log(`Utility results: ${summary.utility_results_count}`);
    console.log(`Total scenarios run: ${summary.total_scenarios_run}`);
    console.log('');
    console.log('Scenario bucket distribution (by bill-impact delta %):');
    for (const [k, v] of Object.entries(bucketCounts)) {
        console.log(`  ${k.padEnd(14)} ${v}`);
    }
    console.log('');
    console.log('Utility bucket distribution (by WORST scenario per utility):');
    for (const [k, v] of Object.entries(utilityBuckets)) {
        console.log(`  ${k.padEnd(14)} ${v}`);
    }
    console.log('');
    console.log('Top 10 utilities by max delta:');
    for (const u of results.slice(0, 10)) {
        console.log(
            `  ${u.id.padEnd(45)}  max ${(u.max_abs_pct * 100).toFixed(1).padStart(6)}%  state=${u.state}  iso=${u.iso_rto}`,
        );
    }
    console.log('');
    if (utilityErrors.length > 0) {
        console.log(`Skipped/errored (${utilityErrors.length}):`);
        for (const e of utilityErrors) console.log(`  - ${e}`);
    }
    console.log('');
    console.log(`Full JSON: ${docsPath}`);
}

main();
